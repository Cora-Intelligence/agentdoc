/**
 * Get an inline SVG icon by name. Returns empty string if not found.
 */
export function icon(name: string): string {
  return SVG_ICONS[name] ?? '';
}

/**
 * Wrap an icon in a colored circle background.
 * Returns an HTML string with the SVG inside a styled span.
 */
export function iconInCircle(
  name: string,
  bgColor: string,
  iconColor: string,
  size = 32
): string {
  const svg = SVG_ICONS[name];
  if (!svg) return '';

  // Scale the SVG to fit inside the circle (60% of circle size)
  const svgSize = Math.round(size * 0.6);
  const scaledSvg = svg
    .replace(/width="\d+"/, `width="${svgSize}"`)
    .replace(/height="\d+"/, `height="${svgSize}"`);

  return `<span style="display:inline-flex;align-items:center;justify-content:center;width:${size}px;height:${size}px;border-radius:50%;background:${bgColor};color:${iconColor};flex-shrink:0;">${scaledSvg}</span>`;
}
